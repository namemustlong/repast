package com.common.model;

public class GoodDetailExt {
    private Integer id;

    private Integer allSellNum;

    private Float score;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAllSellNum() {
        return allSellNum;
    }

    public void setAllSellNum(Integer allSellNum) {
        this.allSellNum = allSellNum;
    }

    public Float getScore() {
        return score;
    }

    public void setScore(Float score) {
        this.score = score;
    }
}